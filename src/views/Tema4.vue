    p.mb-4(data-aos="fade-down") Esta norma se aplica a diversos aspectos de los trabajos académicos, incluyendo la estructura general del documento, la forma de citar fuentes y referencias bibliográficas, la disposición del contenido, la numeración de páginas, el tipo de letra y el tamaño, los márgenes y otros aspectos relacionados con la presentación visual del trabajo.


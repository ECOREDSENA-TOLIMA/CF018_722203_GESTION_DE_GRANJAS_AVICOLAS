      h2 4.3 Estructura bajo Norma Técnica de Calidad (NTC 1486)

    p.mb-4(data-aos="fade-down") La Norma Técnica de Calidad (NTC) 1486 establece las pautas para la estructura y presentación de trabajos académicos escritos, independientemente de su nivel de profundidad. Esta norma abarca una variedad de tipos de trabajos, que incluyen desde trabajos de introducción a la investigación hasta tesis, informes científicos, ensayos y otros trabajos similares.

